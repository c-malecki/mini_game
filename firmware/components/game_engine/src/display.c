void Display_Init(void) {
  i2c_master_bus_config_t i2c_bus_cfg = {
      .clk_source = I2C_CLK_SRC_DEFAULT,
      .i2c_port = I2C_NUM_0,
      .sda_io_num = DISPLAY_SDA_PIN,
      .scl_io_num = DISPLAY_SCL_PIN,
      .glitch_ignore_cnt = 7,
      .intr_priority = 0,
      .trans_queue_depth = 0,
      .flags =
          {
              .enable_internal_pullup = false,
              .allow_pd = false,
          },
  };
  esp_err_t err = i2c_new_master_bus(&i2c_bus_cfg, &i2c_bus_handle);
  if (err != ESP_OK) {
    ESP_LOGE("DISPLAY", "I2C new master bus error: 0x%X :: %d", err, err);
  }

  // Create the i2c io handle
  esp_lcd_panel_io_handle_t io_handle = NULL;
  esp_lcd_panel_io_i2c_config_t io_config = ESP_SH1106_DEFAULT_IO_CONFIG;
  ESP_ERROR_CHECK(
      esp_lcd_new_panel_io_i2c(i2c_bus_handle, &io_config, &io_handle));

  /* SCREEN CONFIGURATION */

  // sh1106 panel configuration (most of the values are not used, but must be
  // set to avoid cpp warnings)
  esp_lcd_panel_dev_config_t panel_config = {
      .reset_gpio_num = -1, // sh1106 does not have a reset pin, so set to -1
      .rgb_ele_order = LCD_RGB_ELEMENT_ORDER_RGB, // not even used, but must be
                                                  // set to avoid cpp warnings
      .data_endian = LCD_RGB_DATA_ENDIAN_LITTLE,  // not even used, but must be
                                                  // set to avoid cpp warnings
      .bits_per_pixel =
          SH1106_PIXELS_PER_BYTE / 8, // bpp = 1 (monochrome, that's important)
      .flags =
          {
              .reset_active_high =
                  false, // not even used, but must be set to avoid cpp warnings
          },
      .vendor_config =
          NULL, // no need for custom vendor config, not implemented
  };

  // Create the panel handle from the sh1106 driver
  ESP_ERROR_CHECK(
      esp_lcd_new_panel_sh1106(io_handle, &panel_config, &panel_handle));

  // Reset the screen (no reset pin, so it's a no-op here, optional)
  ESP_ERROR_CHECK(esp_lcd_panel_reset(panel_handle));

  // Initialize the screen (this one isn't optional at all!)
  ESP_ERROR_CHECK(esp_lcd_panel_init(panel_handle));

  // Turn on the screen (Easier to see something, right?)
  ESP_ERROR_CHECK(esp_lcd_panel_disp_on_off(panel_handle, true));

  Display_DrawRect(20, 20, 10, 10, true);
  Display_Flush();
}

void Display_DrawPixl(int x, int y, bool on) {
  if (x < 0 || x >= DISPLAY_WIDTH || y < 0 || y >= DISPLAY_HEIGHT)
    return;

  int index = (y / 8) * DISPLAY_WIDTH + x;
  uint8_t bit = 1 << (y % 8);

  if (on) {
    buffer[index] |= bit;
  } else {
    buffer[index] &= ~bit;
  }
}

void Display_DrawLine(int x0, int y0, int x1, int y1, bool on) {
  int dx = abs(x1 - x0), sx = x0 < x1 ? 1 : -1;
  int dy = -abs(y1 - y0), sy = y0 < y1 ? 1 : -1;
  int err = dx + dy;

  while (1) {
    Display_DrawPixl(x0, y0, on);
    if (x0 == x1 && y0 == y1)
      break;
    int e2 = 2 * err;
    if (e2 >= dy) {
      err += dy;
      x0 += sx;
    }
    if (e2 <= dx) {
      err += dx;
      y0 += sy;
    }
  }
}

void Display_DrawRect(int x, int y, int w, int hgt, bool fill) {
  if (fill) {
    for (int j = y; j < y + hgt; j++) {
      Display_DrawLine(x, j, x + w - 1, j, true);
    }
  } else {
    Display_DrawLine(x, y, x + w - 1, y, true);
    Display_DrawLine(x, y + hgt - 1, x + w - 1, y + hgt - 1, true);
    Display_DrawLine(x, y, x, y + hgt - 1, true);
    Display_DrawLine(x + w - 1, y, x + w - 1, y + hgt - 1, true);
  }
}

void Display_ClearRect(int x, int y, int w, int hgt) {
  for (int j = y; j < y + hgt; j++) {
    for (int i = x; i < x + w; i++) {
      Display_DrawPixl(i, j, false);
    }
  }
}

void Display_DrawCirc(int xc, int yc, int r, bool fill) {
  int x = r, y = 0, err = 0;

  while (x >= y) {
    if (fill) {
      Display_DrawLine(xc - x, yc + y, xc + x, yc + y, true);
      Display_DrawLine(xc - x, yc - y, xc + x, yc - y, true);
      Display_DrawLine(xc - y, yc + x, xc + y, yc + x, true);
      Display_DrawLine(xc - y, yc - x, xc + y, yc - x, true);
    } else {
      Display_DrawPixl(xc + x, yc + y, true);
      Display_DrawPixl(xc + y, yc + x, true);
      Display_DrawPixl(xc - y, yc + x, true);
      Display_DrawPixl(xc - x, yc + y, true);
      Display_DrawPixl(xc - x, yc - y, true);
      Display_DrawPixl(xc - y, yc - x, true);
      Display_DrawPixl(xc + y, yc - x, true);
      Display_DrawPixl(xc + x, yc - y, true);
    }

    y += 1;
    if (err <= 0) {
      err += 2 * y + 1;
    }
    if (err > 0) {
      x -= 1;
      err -= 2 * x + 1;
    }
  }
}

void Display_DrawChar(int x, int y, char c, bool on) {
  unsigned char idx = (unsigned char)c;
  if (idx > 127)
    return;

  const unsigned char *glyph = &font7x12[idx * 9];
  bool descender = (glyph[0] & 0x80) != 0;
  int y_offset = descender ? FONT_7X12_DESCENDER_OFFSET : 0;

  for (int row = 0; row < FONT_7X12_HEIGHT; row++) {
    uint8_t row_bits =
        glyph[row] & 0x7F; // bit 7 is descender flag, not pixel data
    for (int col = 0; col < FONT_7X12_WIDTH; col++) {
      if (row_bits & (1 << (6 - col))) {
        Display_DrawPixl(x + col, y + row + y_offset, on);
      }
    }
  }
}

void Display_DrawText(int x, int y, const char *text, bool on) {
  int cursor_x = x;
  while (*text) {
    Display_DrawChar(cursor_x, y, *text, on);
    cursor_x += FONT_7X12_WIDTH + 1; // 7px glyph + 1px spacing
    text++;
  }
}

void Display_DrawFloat(int x, int y, float value, int decimals, bool on) {
  char buf[16];
  snprintf(buf, sizeof(buf), "%.*f", decimals, value);
  Display_DrawText(x, y, buf, on);
}

void Display_Flush() {
  ESP_ERROR_CHECK(esp_lcd_panel_draw_bitmap(panel_handle, 0, 0, DISPLAY_WIDTH,
                                            DISPLAY_HEIGHT, buffer));
}